                <h2>1. Hamstring Stretch</h2>
                <p>Stand tall and extend one leg forward with the heel on the ground. Lean forward gently from your hips to stretch the hamstrings. Hold for 20-30 seconds on each leg.</p>

                <h2>2. Quadriceps Stretch</h2>
                <p>Stand on one leg and pull the other foot towards your buttocks, keeping your knees together. Hold for 20-30 seconds to stretch the front thigh muscles.</p>

                <h2>3. Shoulder Stretch</h2>
                <p>Bring one arm across your chest and hold it with the opposite arm, stretching the shoulder. Hold for 15-20 seconds on each side to relieve tension and improve shoulder flexibility.</p>

                <h2>4. Side Stretch</h2>
                <p>Stand with your feet shoulder-width apart and raise one arm overhead. Lean gently to the opposite side to stretch your torso and obliques. Repeat on the other side.</p>

                <h2>5. Cat-Cow Stretch</h2>
                <p>On all fours, alternate arching your back upwards (cat) and lowering it downwards (cow) to mobilize the spine and strengthen core muscles. Repeat 10-12 times.</p>

