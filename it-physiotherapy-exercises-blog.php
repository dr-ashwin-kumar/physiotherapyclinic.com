                <h2>1. Neck Stretch</h2>
                <p>Sitting in front of the computer for hours can cause neck stiffness. Slowly tilt your head to one side, hold for 15-20 seconds, and repeat on the other side. This improves neck flexibility and reduces tension.</p>

                <h2>2. Shoulder Rolls</h2>
                <p>Raise your shoulders towards your ears, roll them backward, and then forward. Repeat 10-15 times. Shoulder rolls relieve stiffness and improve blood circulation.</p>

                <h2>3. Seated Spinal Twist</h2>
                <p>Sit upright and place your right hand on the back of your chair, twisting your torso gently to the right. Hold for 10 seconds and repeat on the left. This helps stretch the spine and improve posture.</p>

                <h2>4. Wrist and Finger Stretch</h2>
                <p>Extend your arms in front of you and gently pull back each hand's fingers to stretch the wrists. Repeat 10-12 times. This exercise reduces the risk of repetitive strain injuries common in IT professionals.</p>

                <h2>5. Standing Hamstring Stretch</h2>
                <p>Stand up and place one foot on a low surface, keeping your leg straight. Lean forward slightly to stretch the hamstrings. Hold for 15-20 seconds and repeat on the other leg. This relieves lower back tension caused by prolonged sitting.</p>

